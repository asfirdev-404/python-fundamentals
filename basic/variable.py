# VARIABLE
# variable adalah nama untuk menyimpan data

# contoh
Variable = "Hello World"
# cara mengeluarkan datanya dengan cara menggunakan print(variable)
print(Variable)

# data itu di bagi menjadi tipe data dan tipe data itu ada 4 jenisnya, yaitu :

# Integer/int
# integer adalah tipe data yang berisi bilangan bulat 
# berfungsi untuk oprasi atau mengeluarkan output bilangan bulat
# contoh 
Integer = 123
print(Integer)
print(f"Type data Integer = {type(Integer)}") #cara mengetahui type data sebuah variable kita bisa menggunakan type()

# Float/float
# float adalah tipe data yang tidak jauh dengan integer bedanya float adalah bilangan desimal yaitu bilangan berkoma
# berfungsi untuk oprasi atau mengeluarkan output dengan bilangan desimal
# contoh
Float = 3.14 # perbedaan dengan bilangan desimal umumnya
print(Float)
print(f"Type data Float = {type(Float)}")

# String/str
# string adalah tipe data yang berisi tentang teks
# berfungsi untuk menerima dan memberi output yang isinya bisa angka dan huruh dalam sekali output
# contoh
String = "Happy Happy"
print(String)
print(f"Type data String = {type(String)}")

# Boolean/bool
# boolean adalah tipe data yang hanya menghasilkan nilai True atau False
# berfungsi untuk pengambilan keputusan atau proses logika
# contoh
Boolean = True
print(Boolean)
print(f"Type data Boolean = {type(Boolean)}")
